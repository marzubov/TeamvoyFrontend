function game_toggle(game, force) {
  var interval = game.getInterval();
  if (force == "stop" ||interval !== null) {
    clearInterval(interval);
    game.setTheInterval(null);
  } else {
    interval = setInterval(game.step, 100);
    game.setTheInterval(interval);
  }
}

function add_listeners(game, toggle, step, setIntervalButton, generate) {
  document.getElementById(toggle).addEventListener("click", function(){
    game_toggle(game);
  });
  document.getElementById(step).addEventListener("click", function() {
    game_toggle(game, "stop");
    game.step();
  });
  document.getElementById(setIntervalButton).addEventListener("click", function(){
    var time = parseInt(document.getElementById("interval").value);
    var interval = setInterval(game.step, time);
    game.setTheInterval(interval);
  });
  document.getElementById(generate).addEventListener("click", function() {
    var example1_cells = [], N = 19, M = 44;
    for (var i = 0; i < N; i++) {
      example1_cells.push([]);
      for (var j = 0; j < M; j++) {
        example1_cells[i].push(Math.round(Math.random()));
        console.log(Math.round(Math.random()));
      }
    }
    game.setNewFigure(example1_cells);
  });
}


